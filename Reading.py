def add(num1, num2): # for adding 
    return num1 + num2

def sub(num3, num4):
    return num3 - num4

def mult(num5, num6):
    return num5 * num6

def divi(num7, num8):
    return num7 / num8
    
    
    

def main():
    myResults = add(20, 20)
    print(myResults)

    mySample = sub(20, 20)
    print(mySample)

    myValue = mult(20, 20)
    print(myValue)

    myDivision = divi(20, 20)
    print(myDivision)

main()
    

    